package javaPackage;

public class RelationalOperator {

	public static void main(String[] args) {
	
		int firstNumber= 20;
		int secondNumber =30;
		
		System.out.println(firstNumber > secondNumber);
		System.out.println(firstNumber < secondNumber);
		System.out.println(firstNumber >= secondNumber);
		System.out.println(firstNumber >= secondNumber);
		System.out.println(firstNumber == secondNumber);
	}

}
